%==========================================================================
    function dec = util_bin_to_int(bin)
%==========================================================================
% binary decoding function
%
% inputs    description
% --------------------------------
% bin       list of binary values
%
%
% output
% --------------------------------
% dec       decimal integer value
%--------------------------------------------------------------------------
    dec = 0;
    power = 1;
%--------------------------------------------------------------------------
    for i=length(bin):-1:1
        if( bin(i) )
            dec = dec + power;
        end %if
    power = 2*power;
    end %for
%--------------------------------------------------------------------------

